// 1-Manipulação de Strings 

split() // Divide uma string em um array de substrings.

toLowerCase() // Converte a string para minúsculas.

toUpperCase() // Converte a string para maiúsculas.

trim() // Remove espaços em branco do início e do fim de uma string.



// 2-Manipulação de Números

parseInt() // Converte uma string para um número inteiro.

parseFloat() // Converte uma string para um número de ponto flutuante.

isNaN() // Verifica se um valor é NaN.



// 3-Manipulação de Arrays

splice() // Adiciona ou remove elementos de um array.

map() // Cria um novo array com os resultados de uma função aplicada a cada elemento.

reduce() // Aplica uma função a um acumulador e a cada valor de um array (da esquerda para a direita) para reduzi-lo a um único valor.



// 4-Manipulação de Objetos

Object.keys() // Retorna um array com os nomes das propriedades enumeráveis de um objeto.

Object.values() // Retorna um array com os valores das propriedades enumeráveis de um objeto.

JSON.parse() // Converte uma string JSON para um objeto JavaScript.



// 5-Entrada e Saída de Dados

alert() // Exibe uma caixa de diálogo de alerta com uma mensagem.

prompt() // Exibe uma caixa de diálogo com uma mensagem opcional solicitando a entrada de um usuário.

confirm() // Exibe uma caixa de diálogo com uma mensagem e dois botões (OK e Cancelar).
